    public class ClassWrapper
    {
        public Class<? extends IFScript> klass;

        public ClassWrapper(Class<? extends IFScript> klass)
        {
            this.klass = klass;
        }

        @Override
        public String toString()
        {
            ScriptMetadata metadata = klass.getAnnotation(ScriptMetadata.class);
            if(metadata != null)
            {
                return metadata.Name();
            }
            return klass.getName();
        }
    }

