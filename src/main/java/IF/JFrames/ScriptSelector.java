    private void ScriptListValueChanged(javax.swing.event.ListSelectionEvent evt)
    {//GEN-FIRST:event_ScriptListValueChanged
        var value = ScriptList.getSelectedValue();
        if(value != null && value.klass != null)
        {
            ScriptMetadata metadata = value.klass.getAnnotation(ScriptMetadata.class);
            if(metadata != null)
            {
                ScriptNameField.setText(metadata.Name());
                ScriptDescriptionField.setText(metadata.Description());
            }
            EnterButton.setEnabled(true);

        }
        else
        {
            EnterButton.setEnabled(false);
        }
    }//GEN-LAST:event_ScriptListValueChanged

